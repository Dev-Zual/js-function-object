const color = "red";

switch (color) {
  case "green":
    console.log("you are green");
    break;
  case "white":
    console.log("you are white");
    break;
  case "blue":
    console.log("you are blue");
    break;
  case "red":
    console.log("you are red");
    break;
  default:
    console.log("you are black");
    break;
}
